"""Factory for system transcript entries.

This module handles creation of MessageContent from SystemTranscriptEntry:
- SystemMessage: Regular system messages with level (info, warning, error)
- HookSummaryMessage: Hook execution summaries

Also provides:
- is_system_message: Check if text content is a system message to filter
"""

from typing import Optional, Union

from ..models import (
    HookInfo,
    HookSummaryMessage,
    SystemMessage,
    SystemTranscriptEntry,
)
from .meta_factory import create_meta
from .user_factory import simplify_command_tags


# =============================================================================
# System Message Detection
# =============================================================================


def is_system_message(text_content: str) -> bool:
    """Check if a message is a system message that should be filtered out."""
    system_message_patterns = [
        "Caveat: The messages below were generated by the user while running local commands. DO NOT respond to these messages or otherwise consider them in your response unless the user explicitly asks you to.",
        "[Request interrupted by user for tool use]",
        "<local-command-stdout>",
    ]

    return any(text_content.startswith(pattern) for pattern in system_message_patterns)


# =============================================================================
# System Message Creation
# =============================================================================


def create_system_message(
    transcript: SystemTranscriptEntry,
) -> Optional[Union[SystemMessage, HookSummaryMessage]]:
    """Create a MessageContent from a system transcript entry.

    Handles:
    - Hook summaries (subtype="stop_hook_summary")
    - Regular system messages with level-specific styling (info, warning, error)

    Args:
        transcript: The system transcript entry to process

    Returns:
        SystemMessage or HookSummaryMessage (with meta attached),
        or None if the message should be skipped (e.g., silent hook successes)

    Note:
        Slash command messages (<command-name>, <local-command-stdout>) are user messages,
        not system messages. They are handled separately.
    """
    if transcript.subtype == "stop_hook_summary":
        # Skip silent hook successes (no output, no errors)
        if not transcript.hasOutput and not transcript.hookErrors:
            return None
        # Create structured hook summary content
        meta = create_meta(transcript)
        hook_infos = [
            HookInfo(command=info.get("command", "unknown"))
            for info in (transcript.hookInfos or [])
        ]
        return HookSummaryMessage(
            has_output=bool(transcript.hasOutput),
            hook_errors=transcript.hookErrors or [],
            hook_infos=hook_infos,
            meta=meta,
        )

    if not transcript.content:
        # Skip system messages without content (shouldn't happen normally)
        return None

    # Create structured system content
    meta = create_meta(transcript)
    level = transcript.level or "info"

    # ``local_command`` system entries carry the harness's
    # ``<command-name>X</command-name><command-message>X</command-message>
    # <command-args>Y</command-args>`` triple (for typed slash commands)
    # or a ``<local-command-stdout>X</local-command-stdout>`` payload
    # (for dialog-dismissal hints like ``"Status dialog dismissed"``).
    # Render those as ``"/X"`` / ``"X"`` rather than the raw XML —
    # closes #129. Other subtypes (``compact_boundary``,
    # ``stop_hook_summary``, …) carry plain text and pass through
    # unchanged via the no-match fallback in ``simplify_command_tags``.
    #
    # The subtype gate is **intent-documenting, not safety-providing**:
    # ``simplify_command_tags`` is opportunistic by design (returns its
    # input unchanged when none of the patterns match), so applying it
    # unconditionally would also be safe. The gate is here to make
    # "we only expect this XML soup in ``local_command`` entries"
    # explicit, and to keep the audit trail tight if the harness ever
    # starts emitting command tags under a different subtype.
    text = (
        simplify_command_tags(transcript.content)
        if transcript.subtype == "local_command"
        else transcript.content
    )

    # Surface compact-boundary metadata on the SystemMessage so the nav
    # label on the following CompactedSummaryMessage can read preTokens
    # and trigger without having to crawl back to the transcript entry.
    pre_tokens: Optional[int] = None
    compact_trigger: Optional[str] = None
    if transcript.subtype == "compact_boundary" and transcript.compactMetadata:
        raw_pre = transcript.compactMetadata.get("preTokens")
        # `bool` is a subclass of `int` in Python — `isinstance(True, int)`
        # returns True — so JSON true/false would slip through as 1/0. Filter
        # bools explicitly; we only want genuine integer token counts.
        if isinstance(raw_pre, int) and not isinstance(raw_pre, bool):
            pre_tokens = raw_pre
        raw_trigger = transcript.compactMetadata.get("trigger")
        if isinstance(raw_trigger, str):
            compact_trigger = raw_trigger

    return SystemMessage(
        level=level,
        text=text,
        meta=meta,
        compact_pre_tokens=pre_tokens,
        compact_trigger=compact_trigger,
    )
